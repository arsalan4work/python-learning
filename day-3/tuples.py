# Tuples in Python
# A built-in data type that lets us create immutable sequence of values.

tup = (1,2,3,4,4)
print(tup[0])

# Tuple Slicing Method
print(tup[1:3])

# Tuple Methods index(), count()
print(tup.index(2)) # I want to find 2 in my values. He says his location is on 1.
print(tup.count(4)) #Counts the specific value. I want to find 4, so in my tuple 4 comes 2 times print 2.